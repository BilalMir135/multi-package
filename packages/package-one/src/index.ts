function sum(num_a: number, number_b: number) {
  return num_a + number_b
}

function sub(num_a: number, number_b: number) {
  return num_a - number_b
}

function mul(num_a: number, number_b: number) {
  return num_a * number_b
}

function div(num_a: number, number_b: number) {
  return num_a / number_b
}

export default { sum, sub, mul, div }
